    <div class="businessmodel-four bc bbe0 mb40">等额本息是指企业融资满额后，每个月固定时间按相等金额偿还贷款本息，其中每个月贷款利息按月初剩余贷款本金计算并逐月结清。<br />
采用等额本息的还款方式是一般针对借款期限较长、每月具有稳定现金回款的企业，最大程度的提高出借人的资金安全。<br />
等额本息月月有回款，资金更灵活，适合具有房贷、车贷的理财客户。
</div>
<div class="businessmodel-five  bc mb60">先息后本是每月还利息，到期再还本金。企业融资满额后，每个月固定时间偿还利息，最后一个月偿还当月利息及全部本金。
采用先息后本的还款方式一般是针对借款期限较短、资金快速回笼的企业。先息后本一次投资，安心获收益，类似银行定期存款，适合短期内没有资金需求的客户。
