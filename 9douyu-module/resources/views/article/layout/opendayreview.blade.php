$js = <<<'BLOCK'
(function($){

$(document).ready(function(){
    //花絮图片切换
    jQuery("#openday-slide2").jCarouselLite({
        // auto:5000,
        speed:1000,
        visible:3,
        scroll:3,
        vertical:false,
        //stop:$("#index_tabs1"),
        //btnGo:$("#index_tabs1_li li"),
        //btnGoOver:true,
        btnPrev:"#openday-slide2_prev",
        btnNext:"#openday-slide2_next"
    });

    //对文章中的每个花絮图片加a标签，用于触发nyroModal弹窗
    $.nmProxy(".openday-pic-main img");
    
    var isMobile = function() {
        var sUserAgent = navigator.userAgent.toLowerCase();
        var bIsIpad = sUserAgent.match(/ipad/i) == "ipad";
        var bIsIphoneOs = sUserAgent.match(/iphone os/i) == "iphone os";
        var bIsMidp = sUserAgent.match(/midp/i) == "midp";
        var bIsUc7 = sUserAgent.match(/rv:1.2.3.4/i) == "rv:1.2.3.4";
        var bIsUc = sUserAgent.match(/ucweb/i) == "ucweb";
        var bIsAndroid = sUserAgent.match(/android/i) == "android";
        var bIsCE = sUserAgent.match(/windows ce/i) == "windows ce";
        var bIsWM = sUserAgent.match(/windows mobile/i) == "windows mobile";
        if (bIsIpad || bIsIphoneOs || bIsMidp || bIsUc7 || bIsUc || bIsAndroid || bIsCE || bIsWM) {
            return true;
        }
        
        return false;
    }
    
    $("a.openday-img").click(function(event){
        if(isMobile()) {
            $(this).attr("target", "_blank");
            return true;
        }
        event.preventDefault();
        if($(".openday-main").data("openday-loading")) return false;
        $(".openday-pop-main").addClass("openday-loading");
        $(".pop-wrap").show();
        $(".openday-main").data("openday-loading", true);
        $.get($(this).attr("href"), function(data){
            $(".openday-pop-main").removeClass("openday-loading");
            $(".openday-main").data("openday-loading", null).html(data).css("width", "999999px");
            if($(".openday-main li").size() % 2 == 1) {
                $(".openday-main ul").append("<li>&nbsp;</li>");
            }
            jQuery("#openday-slide1").jCarouselLite({    
                speed:300,
                vertical:false,
                visible:2,
                scroll:2,
                circular: false,
                btnPrev:"#openday-slide1_prev",
                btnNext:"#openday-slide1_next"
            });
        });
    });
    
    $(".openday-close,.pop-mask").click(function(){
        $(".pop-wrap").hide();
        $(".openday-main").data("openday-loading", null);
    })
});
})(jQuery);
BLOCK;
$jscssminify->addScriptDeclaration($js);
?>
</block>