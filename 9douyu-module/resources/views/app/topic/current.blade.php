            <p>A：加入当日计息，用户每日的收益在次日凌晨00:00计算并发放至零钱计划总额，随后会显示在昨日收益位置并可查看零钱计划金额投资的债权，只要零钱计划总额大于0元时都可以申请转出，不受时间限制。</p>
            <h3>Q3：我加入零钱计划的时候收益率是7%，是不是以后都会是7%的收益率？</h3>
            <p>A：不会，我们的收益每日计算，次日0点返还至零钱计划账户，当日收益＝当日零钱计划总额（每日24点结算）＊借款利率／365 ，因此加入当日显示的利率为加入当日零钱计划计算收益的借款利率。注意：每日收益四舍五入后不足0.01元不计入零钱计划账户。</p>
