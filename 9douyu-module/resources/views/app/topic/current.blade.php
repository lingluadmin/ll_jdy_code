                <li>• 第二种情况：为避免平台发生流动性风险，系统设置每日转出总额度为“全部用户持有的零钱计划总额的20%＋当日全部用户自动加入零钱计划的金额”，一旦当日转出额度用尽，用户无法申请转出，在次日开放新的转出额度后可重新申请转出零钱计划。</li>
